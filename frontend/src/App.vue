<template>
  <div id="app">
    <!-- –°—Ç—Ä–∞–Ω–∏—Ü–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ -->
    <AuthPage 
      v-if="!isAuthenticated" 
      @authenticated="onAuthenticated"
    />
    
    <!-- –û—Å–Ω–æ–≤–Ω–æ–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ -->
    <MainApp 
      v-else 
      :user="currentUser"
      @logout="onLogout"
    />
  </div>
</template>

<script>
import { ref, onMounted } from 'vue'
import AuthPage from './components/AuthPage.vue'
import MainApp from './components/MainApp.vue'

export default {
  name: 'App',
  components: {
    AuthPage,
    MainApp
  },
  setup() {
    const isAuthenticated = ref(false)
    const currentUser = ref(null)

    // –û–±—Ä–∞–±–æ—Ç–∫–∞ —É—Å–ø–µ—à–Ω–æ–π –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏
    const onAuthenticated = (user) => {
      console.log('‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω:', user)
      currentUser.value = user
      isAuthenticated.value = true
    }

    // –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Å–∏—Å—Ç–µ–º—ã
    const onLogout = () => {
      console.log('üëã –í—ã—Ö–æ–¥ –∏–∑ —Å–∏—Å—Ç–µ–º—ã')
      currentUser.value = null
      isAuthenticated.value = false
    }

    // –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–π –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ
    onMounted(() => {
      try {
        console.log('üöÄ App.vue –∑–∞–≥—Ä—É–∂–∞–µ—Ç—Å—è')
        
        const savedUser = localStorage.getItem('shiftwork_user')
        if (savedUser) {
          try {
            const user = JSON.parse(savedUser)
            onAuthenticated(user)
            console.log('‚úÖ –ù–∞–π–¥–µ–Ω —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å')
          } catch (e) {
            console.log('‚ùå –û—à–∏–±–∫–∞ –ø–∞—Ä—Å–∏–Ω–≥–∞ —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è')
            localStorage.removeItem('shiftwork_user')
          }
        }
      } catch (error) {
        console.error('‚ùå –û—à–∏–±–∫–∞ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ App:', error)
      }
    })

    return {
      isAuthenticated,
      currentUser,
      onAuthenticated,
      onLogout
    }
  }
}
</script>

