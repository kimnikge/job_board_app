import { useRouter, useRoute } from 'vue-router'
import { useAuthStore } from '@/stores/auth'
import { useNotificationsStore } from '@/stores/notifications'

const router = useRouter()
const route = useRoute()
const authStore = useAuthStore()
const notificationsStore = useNotificationsStore()

// –°–æ—Å—Ç–æ—è–Ω–∏–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
const telegramContainer = ref(null)
const telegramWidgetLoaded = ref(false)
const loading = computed(() => authStore.loading)
const error = computed(() => authStore.error)

// –ü—Ä–æ–≤–µ—Ä—è–µ–º demo —Ä–µ–∂–∏–º
const isDemoMode = import.meta.env.VITE_USE_DEMO_MODE === 'true'

console.log('üîß AuthPage mounted with:', {
  isDemoMode,
  VITE_USE_DEMO_MODE: import.meta.env.VITE_USE_DEMO_MODE,
  TELEGRAM_BOT_USERNAME: import.meta.env.VITE_TELEGRAM_BOT_USERNAME
})

// –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è Telegram Widget
const TELEGRAM_BOT_USERNAME = import.meta.env.VITE_TELEGRAM_BOT_USERNAME || 'ShiftworkKZBot' // –ó–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ –≤–∞—à –±–æ—Ç
const WIDGET_CONFIG = {
  size: 'large',
  corner_radius: 20,
  request_access: 'write',
  userpic: true,
  lang: 'ru'
}

const showSuccess = (title, message) => {
  notificationsStore.addNotification({
    type: 'success',
    title,
    message
  })
}

const showError = (title, message) => {
  notificationsStore.addNotification({
    type: 'error',
    title,
    message
  })
}

const clearError = () => {
  authStore.clearError()
}

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —É—Å–ø–µ—à–Ω–æ–π –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ Telegram
const onTelegramAuth = async (userData) => {
  console.log('üîß onTelegramAuth called with:', userData)
  
  try {
    console.log('üîß Calling authStore.loginWithTelegram...')
    const result = await authStore.loginWithTelegram(userData)
    console.log('üîß Auth result:', result)
    
    if (result.success) {
      console.log('üîß Login successful!')
      showSuccess(
        '–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!', 
        `–ü—Ä–∏–≤–µ—Ç, ${userData.first_name}! –í—ã —É—Å–ø–µ—à–Ω–æ –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω—ã —á–µ—Ä–µ–∑ Telegram`
      )
      
      // –ü–µ—Ä–µ–Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É, —Å –∫–æ—Ç–æ—Ä–æ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—Ä–∏—à–µ–ª, –∏–ª–∏ –Ω–∞ –≥–ª–∞–≤–Ω—É—é
      const redirect = route.query.redirect || '/'
      console.log('üîß Redirecting to:', redirect)
      router.push(redirect)
    } else {
      console.log('üîß Login failed:', result.error)
      showError('–û—à–∏–±–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏', result.error || '–ù–µ —É–¥–∞–ª–æ—Å—å –≤–æ–π—Ç–∏ —á–µ—Ä–µ–∑ Telegram')
    }
  } catch (error) {
    console.error('üîß Auth error:', error)
    showError('–û—à–∏–±–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏', '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏')
  }
}

// Fallback –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è (–¥–ª—è —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∏/—Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è)
const handleFallbackLogin = async () => {
  console.log('üîß Fallback login clicked!')
  console.log('üîß Demo mode:', isDemoMode)
  console.log('üîß Widget loaded:', telegramWidgetLoaded.value)
  
  const demoTelegramData = {
    id: 123456789,
    first_name: 'Demo',
    last_name: 'User',
    username: 'demouser',
    photo_url: 'https://via.placeholder.com/150',
    auth_date: Math.floor(Date.now() / 1000),
    hash: 'demo_hash_for_testing'
  }
  
  console.log('üîß Demo data:', demoTelegramData)
  await onTelegramAuth(demoTelegramData)
}

// –ó–∞–≥—Ä—É–∑–∫–∞ Telegram Login Widget
const loadTelegramWidget = () => {
  try {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∑–∞–≥—Ä—É–∂–µ–Ω –ª–∏ —Å–∫—Ä–∏–ø—Ç Telegram
    if (window.TelegramLoginWidget) {
      createTelegramWidget()
      return
    }

    // –°–æ–∑–¥–∞–µ–º —Å–∫—Ä–∏–ø—Ç –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ Telegram Login Widget
    const script = document.createElement('script')
    script.async = true
    script.src = 'https://telegram.org/js/telegram-widget.js?19'
    script.setAttribute('data-telegram-login', TELEGRAM_BOT_USERNAME)
    script.setAttribute('data-size', WIDGET_CONFIG.size)
    script.setAttribute('data-corner-radius', WIDGET_CONFIG.corner_radius)
    script.setAttribute('data-request-access', WIDGET_CONFIG.request_access)
    script.setAttribute('data-userpic', WIDGET_CONFIG.userpic)
    script.setAttribute('data-lang', WIDGET_CONFIG.lang)
    script.setAttribute('data-onauth', 'onTelegramAuth(user)')

    // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≥–ª–æ–±–∞–ª—å–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é –¥–ª—è callback
    window.onTelegramAuth = onTelegramAuth

    script.onload = () => {
      console.log('Telegram widget script loaded')
      telegramWidgetLoaded.value = true
    }

    script.onerror = () => {
      console.error('Failed to load Telegram widget script')
      telegramWidgetLoaded.value = false
    }

    // –î–æ–±–∞–≤–ª—è–µ–º —Å–∫—Ä–∏–ø—Ç –≤ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    if (telegramContainer.value) {
      telegramContainer.value.appendChild(script)
    }
  } catch (error) {
    console.error('Error loading Telegram widget:', error)
    telegramWidgetLoaded.value = false
  }
}

// –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π —Å–ø–æ—Å–æ–± —Å–æ–∑–¥–∞–Ω–∏—è –≤–∏–¥–∂–µ—Ç–∞
const createTelegramWidget = () => {
  if (telegramContainer.value && window.TelegramLoginWidget) {
    window.TelegramLoginWidget.create(telegramContainer.value, {
      bot_id: TELEGRAM_BOT_USERNAME,
      ...WIDGET_CONFIG,
      callback: onTelegramAuth
    })
    telegramWidgetLoaded.value = true
  }
}

onMounted(() => {
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω –ª–∏ —É–∂–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
  if (authStore.isAuthenticated) {
    router.push('/')
    return
  }

  // –ó–∞–≥—Ä—É–∂–∞–µ–º Telegram Widget —Ç–æ–ª—å–∫–æ –≤ production
  if (!isDemoMode) {
    loadTelegramWidget()
  }
})
</script>

