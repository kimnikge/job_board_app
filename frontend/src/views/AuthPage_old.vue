const handleTelegramWebAppLogin = async () => {
  console.log('üîß Telegram Web App login started')
  
  if (!window.Telegram?.WebApp) {
    console.error('Telegram Web App not available')
    showError('–û—à–∏–±–∫–∞', '–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –æ—Ç–∫—Ä—ã—Ç–æ –≤ Telegram')
    return
  }
  
  try {
    const webApp = window.Telegram.WebApp
    const initData = webApp.initData
    
    if (!initData) {
      console.error('No init data from Telegram')
      showError('–û—à–∏–±–∫–∞', '–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–∞–Ω–Ω—ã–µ –æ—Ç Telegram')
      return
    }
    
    console.log('üîß Telegram Web App init data:', initData)
    
    // –ü–∞—Ä—Å–∏–º –¥–∞–Ω–Ω—ã–µ –æ—Ç Telegram Web App
    const urlParams = new URLSearchParams(initData)
    const userParam = urlParams.get('user')
    
    if (!userParam) {
      console.error('No user data in init data')
      showError('–û—à–∏–±–∫–∞', '–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è')
      return
    }
    
    const userData = JSON.parse(userParam)
    console.log('üîß Telegram user data:', userData)
    
    // –î–æ–±–∞–≤–ª—è–µ–º –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –¥–ª—è –≤–∞–ª–∏–¥–∞—Ü–∏–∏
    const telegramData = {
      id: userData.id,
      first_name: userData.first_name,
      last_name: userData.last_name,
      username: userData.username,
      photo_url: userData.photo_url,
      auth_date: Math.floor(Date.now() / 1000),
      hash: urlParams.get('hash') || 'telegram_web_app_hash'
    }
    
    await onTelegramAuth(telegramData)
    
  } catch (error) {
    console.error('üîß Telegram Web App auth error:', error)
    showError('–û—à–∏–±–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏', '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ —á–µ—Ä–µ–∑ Telegram')
  }
}

// –ó–∞–≥—Ä—É–∑–∫–∞ Telegram Login Widget
const loadTelegramWidget = () => {
  try {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∑–∞–≥—Ä—É–∂–µ–Ω –ª–∏ —Å–∫—Ä–∏–ø—Ç Telegram
